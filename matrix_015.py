# Chaque fonction peut avoir:
# - les arguments 
# - un objet qu'elle retourne
#
# Pour definir (creer) une fonction en Python on utilise le mot 'def'
# Pour retourner un objet on utilise le mot 'return'
# Par example:
def multiply_by_2(x) :
    return x*2
# On vient de creer une fonction
# - qui s'appele: multiply_by_2
# - qui prend 1 argument: x
# - qui retourne un object produit par la multiplication de x par 2
# !! Pour l'instant, le python et la fonction ne savient pas combient vaut x
#    (En fait, on veut pas le savoir. On s'en fiche.)
#    Une seul chose important: on prend quelque chose, multiplie ca par 2 et retourne
# !! La fonction ne le sait qu'au moment ou elle est appele
#
# Tu peux utiliser (appeler) cette fonction:
y = multiply_by_2(10)
# On vient de creer une variable 'y'
# et assigner une valuer (qui est retourne par la fonction) a cette variable 'y'

